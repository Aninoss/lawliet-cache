package booru;

public class BooruChoice {

    private String name;
    private String value;

    public String getName() {
        return name;
    }

    public BooruChoice setName(String name) {
        this.name = name;
        return this;
    }

    public String getValue() {
        return value;
    }

    public BooruChoice setValue(String value) {
        this.value = value;
        return this;
    }

}
